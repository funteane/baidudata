package com.cubead.jinjili.domain.model;

public class SuperviseMonitor {
	private String adgroupId;
	private String campaignId;
	private String folderId;
	private String monitorObjectId;
	private String monitorId;
	private String monitorObjectType;
	
	public String getAdgroupId() {
		return adgroupId;
	}
	public void setAdgroupId(String adgroupId) {
		this.adgroupId = adgroupId;
	}
	public String getCampaignId() {
		return campaignId;
	}
	public void setCampaignId(String campaignId) {
		this.campaignId = campaignId;
	}
	public String getFolderId() {
		return folderId;
	}
	public void setFolderId(String folderId) {
		this.folderId = folderId;
	}
	public String getMonitorId() {
		return monitorId;
	}
	public void setMonitorId(String monitorId) {
		this.monitorId = monitorId;
	}
	public String getMonitorObjectId() {
		return monitorObjectId;
	}
	public void setMonitorObjectId(String monitorObjectId) {
		this.monitorObjectId = monitorObjectId;
	}
	public String getMonitorObjectType() {
		return monitorObjectType;
	}
	public void setMonitorObjectType(String monitorObjectType) {
		this.monitorObjectType = monitorObjectType;
	}
}
